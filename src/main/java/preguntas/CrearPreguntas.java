    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //--------------------------------------------------------------------------
        //Ingresar los datos para crear una nueva pregunta
        //--------------------------------------------------------------------------

        String Pregunta = jTextField1.getText();
        String RespuestaCorecta = jTextField2.getText();
        String RespuestaIncorrecta1 = jTextField3.getText();
        String RespuestaIncorrecta2 = jTextField4.getText();
        String RespuestaIncorrecta3 = jTextField5.getText();
        String Nivel = jTextField6.getText();
        String Id = jTextField7.getText();

        Map<String, Object> data = new HashMap<>();
        if ((jTextField1.getText().length() == 0) && (jTextField2.getText().length() == 0)
                && (jTextField3.getText().length() == 0) && (jTextField4.getText().length() == 0)
                && (jTextField5.getText().length() == 0) && (jTextField6.getText().length() == 0)
                && (jTextField7.getText().length() == 0)) {
            JOptionPane.showMessageDialog(
                    this,
                    "Faltaron casillas por llenar",
                    "Creacion de preguntas",
                    JOptionPane.PLAIN_MESSAGE
            );
        } else {
            data.put("Pregunta", Pregunta);
            data.put("RespuestaC", RespuestaCorecta);
            data.put("RespuestaI1", RespuestaIncorrecta1);
            data.put("RespuestaI2", RespuestaIncorrecta2);
            data.put("RespuestaI3", RespuestaIncorrecta3);
            data.put("Nivel", Nivel);
            data.put("Id", Id);
            String uniqueID = UUID.randomUUID().toString();
            conexion.insertarDatos("Preguntas" + Nivel, uniqueID, data);
            JOptionPane.showMessageDialog(
                    this,
                    "Pregunta Agregada",
                    "Creacion de preguntas",
                    JOptionPane.PLAIN_MESSAGE
            );
            jTextField1.setText(null);
            jTextField2.setText(null);
            jTextField3.setText(null);
            jTextField4.setText(null);
            jTextField5.setText(null);
            jTextField6.setText(null);
            jTextField7.setText(null);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

